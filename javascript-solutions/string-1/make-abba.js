/**
 * Returns a string made up concatenating the parameters in the order 
 * str1 str2 str2 str1.
 * 
 * @param {String} str1 - First string parameter.
 * @param {String} str2 - Second string parameter.
 * @returns {String} - Returns concatenated string.
 */
function makeAbba (str1, str2) {
  return str1 + str2 + str2 + str1;
}